public class VariableValues {
    public static void main(String[] args) {
        byte box1=123;
        box1=111;
        System.out.println(box1);
        short perPiece=2456;
        perPiece=14562;
        System.out.println(perPiece);
        int wholeNumber=45621;
        wholeNumber=4584250;
        System.out.println(wholeNumber);
        long bigAmount=786245015l;
        bigAmount=8455685862l;
        System.out.println(bigAmount);
        float priceOfBottle=456.23f;
        priceOfBottle=584.215f;
        System.out.println(priceOfBottle);
        double longDecimal=45562.255475d;
        longDecimal=84556.25542d;
        System.out.println(longDecimal);
        char gender='M';
        gender='F';
        System.out.println(gender);
        String phoneNumber="+945-987-876";
        phoneNumber="+9774-9838-98";
        System.out.println(phoneNumber);
        boolean sleepy=true;
        sleepy=false;
        System.out.println(sleepy);
    }
}
